    private void alterarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarButtonActionPerformed
        // TODO add your handling code here:

        Session sessao = fabrica.openSession();
        Aluno aluno = new Aluno();
        aluno.setAluCodigo(Integer.parseInt(textId.getText()));
        aluno.setAluNome(textNome.getText());
        aluno.setAluCidade(textCidade.getText());
        aluno.setAluCurso(textCurso.getText());
        aluno.setAluFone(textFone.getText());

        Transaction tr_aluno = sessao.beginTransaction();
        sessao.update(aluno);
        tr_aluno.commit();
        sessao.close();
        preencherJtable();
    }//GEN-LAST:event_alterarButtonActionPerformed

