    private void excluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirButtonActionPerformed
        // TODO add your handling code here:
        String nome = "Deseja excluir " + textNome.getText() + " ?";
        int escolha = JOptionPane.showConfirmDialog(null, nome, "Exclusão", JOptionPane.YES_OPTION);
        if (escolha == JOptionPane.YES_OPTION) {

            try {
                Session sessao = fabrica.openSession();
                Aluno aluno = new Aluno();
                posicaoAtual = Integer.parseInt(textId.getText());
                aluno.setAluCodigo(Integer.parseInt(textId.getText()));

                Transaction tr_aluno = sessao.beginTransaction();
                sessao.delete(aluno);
                tr_aluno.commit();
                sessao.close();
                JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                preencherJtable();
                textId.setText(Integer.toString(aluno.getAluCodigo()));
                textNome.setText(aluno.getAluNome());
                textCurso.setText(aluno.getAluCurso());
                textCidade.setText(aluno.getAluCidade());
                textFone.setText(aluno.getAluFone());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }


    }//GEN-LAST:event_excluirButtonActionPerformed

